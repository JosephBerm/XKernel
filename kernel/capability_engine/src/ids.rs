/// A type tag for resources, enabling type-safe resource references.
///
/// See Engineering Plan § 3.1.0: Core Domain Model.
/// ResourceType ensures capabilities can only be used with compatible resource types.
#[derive(Clone, PartialEq, Eq, Hash, Debug)]
pub struct ResourceType(String);

impl ResourceType {
    /// Creates a new resource type from a string.
    pub fn new(type_name: impl Into<String>) -> Self {
        ResourceType(type_name.into())
    }

    /// Returns a reference to the underlying string.
    pub fn as_str(&self) -> &str {
        &self.0
    }

    /// Predefined type for file system resources.
    pub fn file() -> Self {
        ResourceType("file".to_string())
    }

    /// Predefined type for memory regions.
    pub fn memory() -> Self {
        ResourceType("memory".to_string())
    }

    /// Predefined type for service endpoints.
    pub fn service() -> Self {
        ResourceType("service".to_string())
    }
}

impl Display for ResourceType {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

