/// Encodes bytes as hex without allocation.
fn hex_encode(bytes: &[u8]) -> String {
    let mut result = String::new();
    for byte in bytes.iter().take(16) {
        // Use a simple hex encoding: just show first 16 bytes
        result.push_str(&format!("{:02x}", byte));
    }
    result
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_cap_id_creation() {
        let bytes = [42u8; 32];
        let cap_id = CapID::from_bytes(bytes);
        assert_eq!(cap_id.as_bytes(), &bytes);
    }

    #[test]
    fn test_cap_id_display() {
        let bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f];
        let cap_id = CapID::from_bytes(bytes);
        let display = cap_id.to_string();
        assert!(display.contains("CapID"));
    }

    #[test]
    fn test_cap_id_debug() {
        let bytes = [42u8; 32];
        let cap_id = CapID::from_bytes(bytes);
        let debug_str = format!("{:?}", cap_id);
        assert!(debug_str.contains("CapID"));
    }

    #[test]
    fn test_cap_id_equality() {
        let bytes1 = [42u8; 32];
        let bytes2 = [43u8; 32];
        let cap_id1 = CapID::from_bytes(bytes1);
        let cap_id2 = CapID::from_bytes(bytes1);
        let cap_id3 = CapID::from_bytes(bytes2);

        assert_eq!(cap_id1, cap_id2);
        assert_ne!(cap_id1, cap_id3);
    }

    #[test]
    fn test_cap_id_hash() {
        use core::collections::hash_map::DefaultHasher;
use alloc::format;
use alloc::string::String;
use alloc::string::ToString;

        let bytes = [42u8; 32];
        let cap_id1 = CapID::from_bytes(bytes);
        let cap_id2 = CapID::from_bytes(bytes);

        let mut h1 = DefaultHasher::new();
        cap_id1.hash(&mut h1);
        let hash1 = h1.finish();

        let mut h2 = DefaultHasher::new();
        cap_id2.hash(&mut h2);
        let hash2 = h2.finish();

        assert_eq!(hash1, hash2);
    }

    #[test]
    fn test_agent_id_creation() {
        let agent = AgentID::new("agent-001");
        assert_eq!(agent.as_str(), "agent-001");
    }

    #[test]
    fn test_agent_id_display() {
        let agent = AgentID::new("system");
        assert_eq!(agent.to_string(), "Agent(system)");
    }

    #[test]
    fn test_resource_id_creation() {
        let resource = ResourceID::new("file:/etc/passwd");
        assert_eq!(resource.as_str(), "file:/etc/passwd");
    }

    #[test]
    fn test_resource_type_predefined() {
        assert_eq!(ResourceType::file().as_str(), "file");
        assert_eq!(ResourceType::memory().as_str(), "memory");
        assert_eq!(ResourceType::service().as_str(), "service");
    }

    #[test]
    fn test_policy_id_creation() {
        let policy = PolicyID::new("mandatory-isolation");
        assert_eq!(policy.as_str(), "mandatory-isolation");
    }

    #[test]
    fn test_ids_are_distinct_types() {
        let agent = AgentID::new("a");
        let resource = ResourceID::new("a");
        // These have different types, so they can't be confused at compile time
        assert_eq!(agent.as_str(), resource.as_str());
    }
}
