//! Strongly typed identifiers for capabilities, agents, and resources.
//!
//! All identifiers in the Cognitive Substrate are typed to prevent accidental confusion
//! and enable compile-time verification.
//! See Engineering Plan § 3.1.0: Core Domain Model & § 3.1.3: Unforgeable Identity.

use alloc::fmt;
use alloc::string::{String, ToString};
use core::fmt::{Debug, Display};
use core::hash::{Hash, Hasher};

/// A 256-bit unforgeable capability identifier, kernel-assigned.
///
/// See Engineering Plan § 3.1.3: Unforgeable Identity & Kernel Assignment.
/// CapIDs are:
/// - Generated by the kernel with cryptographic randomness (never by agents)
/// - Globally unique across all time and instances
/// - Immutable and non-transferable
/// - Used as the primary key for capability revocation & tracking
#[derive(Clone, PartialEq, Eq)]
pub struct CapID([u8; 32]);

impl CapID {
    /// Creates a new CapID from a 32-byte array.
    ///
    /// # Safety
    /// This is called only by the kernel during capability creation.
    /// User code should not construct CapIDs.
    pub const fn from_bytes(bytes: [u8; 32]) -> Self {
        CapID(bytes)
    }

    /// Returns the underlying 32-byte array.
    pub const fn as_bytes(&self) -> &[u8; 32] {
        &self.0
    }

    /// Exposes the capability ID as a reference to the byte slice.
    pub fn as_slice(&self) -> &[u8] {
        &self.0
    }
}

impl Debug for CapID {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.debug_tuple("CapID")
            .field(&hex_encode(&self.0[..8]))
            .finish()
    }
}

impl Display for CapID {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "CapID({})", hex_encode(&self.0))
    }
}

impl Hash for CapID {
    fn hash<H: Hasher>(&self, state: &mut H) {
        self.0.hash(state);
    }
}

/// A strongly-typed agent identifier.
///
/// See Engineering Plan § 3.1.0: Core Domain Model.
/// Agents are principals (users, services, processes) that hold and exercise capabilities.
#[derive(Clone, PartialEq, Eq, Hash, Debug)]
pub struct AgentID(String);

impl AgentID {
    /// Creates a new agent ID from a string.
    pub fn new(id: impl Into<String>) -> Self {
        AgentID(id.into())
    }

    /// Returns a reference to the underlying string.
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl Display for AgentID {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Agent({})", self.0)
    }
}

