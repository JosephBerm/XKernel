    #[test]
    fn test_grant_basic_capability() {
        let mut table = CapabilityTable::new();
        let policy_engine = make_basic_policy_engine();
        let now = Timestamp::new(1000);

        let cap_id = CapID::from_bytes([1u8; 32]);
        let agent = AgentID::new("test-agent");
        let resource_type = ResourceType::file();
        let resource_id = ResourceID::new("test-file");

        let (granted_id, audit) = grant_capability(
            &mut table,
            cap_id.clone(),
            agent.clone(),
            resource_type,
            resource_id,
            OperationSet::all(),
            CapConstraints::new(),
            &policy_engine,
            now,
        )
        .unwrap();

        assert_eq!(granted_id, cap_id);
        assert_eq!(audit.target_agent, agent);
        assert_eq!(audit.timestamp, now);
        assert!(audit.policy_checks_passed);
        assert_eq!(table.len(), 1);
    }

