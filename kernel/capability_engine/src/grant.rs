/// A record of a grant operation, used for audit trails.
///
/// See Engineering Plan § 3.2.3: Grant Operations.
#[derive(Clone, Debug)]
pub struct GrantAuditRecord {
    /// The ID of the granted capability.
    pub cap_id: CapID,

    /// The agent who received the grant.
    pub target_agent: AgentID,

    /// The resource reference (type and ID) being granted.
    pub resource_type: ResourceType,
    pub resource_id: ResourceID,

    /// The operations granted by this capability.
    pub operations: OperationSet,

    /// Constraints applied to the capability.
    pub constraints: CapConstraints,

    /// Timestamp when the grant occurred.
    pub timestamp: Timestamp,

    /// Whether all mandatory policies were satisfied.
    pub policy_checks_passed: bool,
}

impl Display for GrantAuditRecord {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(
            f,
            "Grant({} -> {} on {}:{}, ops={}, policy_ok={})",
            self.cap_id,
            self.target_agent,
            self.resource_type,
            self.resource_id,
            self.operations.bits(),
            self.policy_checks_passed
        )
    }
}

/// Grants a new capability to an agent for a resource.
///
/// This is the kernel-only operation that creates unforgeable capabilities.
/// Per Engineering Plan § 3.1.3, CapIDs are generated by the kernel with
/// cryptographic randomness and cannot be forged by user code.
///
/// The operation is atomic: either fully succeeds or fully fails.
/// Before granting, ALL MandatoryCapabilityPolicies are validated.
///
/// # Arguments
/// * `table` - The capability table where the new capability will be stored
/// * `cap_id` - The kernel-assigned capability ID (must be unique)
/// * `target_agent` - The agent receiving the capability
/// * `resource_type` - The type of resource being granted
/// * `resource_id` - The specific resource ID
/// * `operations` - The set of operations granted
/// * `constraints` - Additional constraints (time bounds, rate limits, etc.)
/// * `policy_engine` - The mandatory policy enforcement engine
/// * `now` - Current timestamp
///
/// # Latency Target
/// <500ns warm cache (per Engineering Plan § 3.2.3)
///
/// See Engineering Plan § 3.1.0 & § 3.2.3: Grant Operations.
pub fn grant_capability(
    table: &mut CapabilityTable,
    cap_id: CapID,
    target_agent: AgentID,
    resource_type: ResourceType,
    resource_id: ResourceID,
    operations: OperationSet,
    constraints: CapConstraints,
    policy_engine: &dyn PolicyEvaluator,
    now: Timestamp,
) -> Result<(CapID, GrantAuditRecord), CapError> {
    // Step 1: Create the capability object (not yet inserted)
    let mut capability = Capability::new(
        cap_id.clone(),
        target_agent.clone(),
        resource_type.clone(),
        resource_id.clone(),
        operations,
        now,
    );
    capability.constraints = constraints.clone();

    // Step 2: Validate against all mandatory policies
    let eval_context = EvalContext::new(
        now.nanos(),
        AgentID::new("kernel"),
        target_agent.clone(),
        "grant",
    );
    let decision = policy_engine.evaluate(&capability, &eval_context);

    if decision.denies_operation() {
        return Err(CapError::PolicyDenied(format!(
            "grant operation denied by policy: {}",
            decision
        )));
    }

    let policy_checks_passed = decision.allows_operation();

    // Step 3: Add initial chain entry (root grant, no parent)
    let root_entry = ChainEntry::new(
        AgentID::new("kernel"),
        target_agent.clone(),
        CapConstraints::new(),
        now,
    );
    capability.chain.add_entry(root_entry)?;

    // Step 4: Create capability table entry
    let entry = CapabilityEntry::new(capability.clone(), target_agent.clone(), now);

    // Step 5: Insert into capability table (atomic point)
    table.insert(cap_id.clone(), entry)?;

    // Step 6: Create and return audit record
    let audit_record = GrantAuditRecord {
        cap_id: cap_id.clone(),
        target_agent,
        resource_type,
        resource_id,
        operations,
        constraints,
        timestamp: now,
        policy_checks_passed,
    };

    Ok((cap_id, audit_record))
}

/// Grants a capability with an optional page table mapping.
///
/// This variant of grant_capability also sets up page table mappings for
/// resources that require memory access control.
///
/// See Engineering Plan § 3.2.3: Grant Operations.
pub fn grant_capability_with_mapping(
    table: &mut CapabilityTable,
    cap_id: CapID,
    target_agent: AgentID,
    resource_type: ResourceType,
    resource_id: ResourceID,
    operations: OperationSet,
    constraints: CapConstraints,
    page_mapping: Option<PageMapping>,
    policy_engine: &dyn PolicyEvaluator,
    now: Timestamp,
) -> Result<(CapID, GrantAuditRecord), CapError> {
    // First, grant the capability normally
    let (granted_cap_id, audit_record) = grant_capability(
        table,
        cap_id.clone(),
        target_agent,
        resource_type,
        resource_id,
        operations,
        constraints,
        policy_engine,
        now,
    )?;

    // Then, if a mapping is provided, add it to the entry
    if let Some(mapping) = page_mapping {
        let entry = table.lookup_mut(&granted_cap_id)?;
        entry.add_page_mapping(mapping);
    }

    Ok((granted_cap_id, audit_record))
}

